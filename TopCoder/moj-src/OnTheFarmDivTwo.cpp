class OnTheFarmDivTwo {
public:
	vector <int> animals(int heads, int legs);
};


vector <int> OnTheFarmDivTwo::animals(int heads, int legs) {
	for(int i=0;i<=heads;i++) {
		int j = heads - i;
		if(2*i + 4*j == legs) {
			vector<int> v;
			v.push_back(i);
			v.push_back(j);
			return v;
		}
	}
	return vector<int>();
}

















