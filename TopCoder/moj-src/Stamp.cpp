class Stamp {
public:
	int getMinimumCost(string desiredColor, int stampCost, int pushCost);
};

int get(char ch){
	if( ch=='R' ) return 0;
	if( ch=='G' ) return 1;
	if( ch=='B' ) return 2;
	return 3;
}

int Stamp::getMinimumCost(string de, int stamp, int push) {
	int ret = 1000000000;
	for(int L=1;L<=sz(de);L++){
		int dp[55][3];
		// 0->R  1->G  2->B 
		clr(dp, 0x3f);
		for(int j=0;j<3;j++){
			int t = get(de[0]);
			if( t==j || t==3 ) dp[0][j]=1;
		}
		for(int i=0;i<sz(de);i++){
			for(int j=0;j<3;j++){
				for(int k=i+1;k<sz(de) && k<=i+L;k++){
					if( k+L>sz(de) ) break;
					int t=get(de[k-1]);
					if( t!=3 && t!=j ) break;
					for(int nj=0;nj<3;nj++){
						if( k<i+L && nj!=j ) continue;
						t=get(de[k]);
						if( t==3 || t==nj) {
							dp[k][nj]=min(dp[k][nj], dp[i][j]+1);
						}
					}
				}
			}
		}
		for(int j=0;j<3;j++){
			bool suc=true;
			for(int k=sz(de)-L;k<sz(de);k++){
				int t=get(de[k]);
				if( t!=3 && t!=j ) suc=false;
			}
			if( suc ) ret=min((LL)ret, L*(LL)stamp + push*(LL)dp[sz(de)-L][j]);
		}
	}
	return ret;
}

















